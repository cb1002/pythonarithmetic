# input for two numbers
num1 = float(input("Enter first number (num1): "))
num2 = float(input("Enter second number (num2): "))

# Multiply 
multiplication = num1 * num2

# Divide 
division = num1 / num2

# Show the answers
print(f"The multiplication of input {num1} and {num2} is: {multiplication}")
print(f"The division of input {num1} and {num2} is: {division}")

