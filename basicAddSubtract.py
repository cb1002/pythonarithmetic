# input for two numbers
num1 = float(input("Enter first number (num1): "))
num2 = float(input("Enter second number (num2): "))

# Add 
addition = num1 + num2

# Subtract 
subtraction = num1 - num2

# Show the answers
print(f"The addition of input {num1} and {num2} is: {addition}")
print(f"The subtraction of input {num1} from {num2} is: {subtraction}")





